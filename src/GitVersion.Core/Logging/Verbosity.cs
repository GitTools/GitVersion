namespace GitVersion.Logging;

/// <summary>
/// Represents verbosity.
/// </summary>
public enum Verbosity
{
    /// <summary>
    /// Quiet verbosity.
    /// </summary>
    Quiet = 0,

    /// <summary>
    /// Minimal verbosity.
    /// </summary>
    Minimal = 1,

    /// <summary>
    /// Normal verbosity.
    /// </summary>
    Normal = 2,

    /// <summary>
    /// Verbose verbosity.
    /// </summary>
    Verbose = 3,

    /// <summary>
    /// Diagnostic verbosity.
    /// </summary>
    Diagnostic = 4
}
